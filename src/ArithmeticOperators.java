
public class ArithmeticOperators {

	public static void main(String[] args) {
	  
		System.out.println(4-5);
		System.out.println(5+6);
		System.out.println(5*9);
		System.out.println(5/4);
		System.out.println(4/5);
		System.out.println(-5/4);
		System.out.println(-4/5);
		System.out.println(5%4);
		System.out.println(4%5);
		System.out.println(-5%4);
		System.out.println(-4%5);
	}

}
