
public class Switch {

	public static void main(String[] args) {
      
		int num = 100;
		
		switch(num)
		{
		 	
		case 100: System.out.println("Case1");	
		break;	
		case 500: System.out.println("Case2");
		break;
		case 200: System.out.println("Case3");
		break;
		case 50:  System.out.println("Case0");
		break;
		default:  System.out.println("default");
			
			
		}
		
		

	}

}
